# Exer. 01 Lê números positivos e adiciona em uma lista.

numeroInteiro = 1
listaDeNumerosInteiros = []
while numeroInteiro > 0:
    numeroInteiro = int(input("Digite um número inteiro positivo: "))
    if numeroInteiro > 0:
        listaDeNumerosInteiros.append(numeroInteiro)

print(listaDeNumerosInteiros)
    

